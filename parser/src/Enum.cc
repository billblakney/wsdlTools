#include "Enum.hh"

#include <iostream>
#include <iterator>
#include <sstream>

//-------------------------------------------------------------------------------
//-------------------------------------------------------------------------------
Enum::Enum()
{
}


//-------------------------------------------------------------------------------
//-------------------------------------------------------------------------------
Enum::Enum(std::string aName)
{
  _Name = aName;
}

//-------------------------------------------------------------------------------
//-------------------------------------------------------------------------------
Enum::~Enum()
{
}

//-------------------------------------------------------------------------------
//-------------------------------------------------------------------------------
void Enum::addValues(std::vector<std::string> aValues)
{
  _Values = aValues;
}

//-------------------------------------------------------------------------------
//-------------------------------------------------------------------------------
void Enum::addValue(std::string aValue)
{
  _Values.push_back(aValue);
}

//-------------------------------------------------------------------------------
//-------------------------------------------------------------------------------
std::string Enum::toString()
{
  std::stringstream tStream;

  tStream << _Name << std::endl;
  std::vector<std::string>::iterator it;
  for( it = _Values.begin(); it != _Values.end(); it++ )
  {
    tStream << *it << std::endl;
  }

  return tStream.str();
}
